def get_buy_links(medicine_names):
    links = {}
    for name in medicine_names:
        try:
            info = get_medicine_links([name])
            if info:
                for item in info:
                    url = item.get("url", "")
                    if any(domain in url for domain in ["1mg.com", "pharmeasy", "netmeds"]):
                        links[name] = url
                        break
                else:
                    links[name] = info[0]["url"]
        except Exception:
            links[name] = ""
    return links

